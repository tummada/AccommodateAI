// Propagate to the Playwright process itself so test-jwt.ts can import
// the private key and db-reset.ts can connect to postgres.
process.env['AUTH_RSA_PRIVATE_KEY'] = keys.privateKey;
process.env['AUTH_RSA_PUBLIC_KEY'] = keys.publicKey;
process.env['E2E_DATABASE_URL'] = LOCAL_PG_URL;

