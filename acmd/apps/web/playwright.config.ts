// __dirname is not defined in ESM — derive it from import.meta.url.
const CONFIG_DIR = dirname(fileURLToPath(import.meta.url));

/**
 * RS-013 Phase 3 — Playwright config for real multi-server E2E.
 *
 * Replaces the ACMD-128 single-web config. Three dev servers are spawned
 * by `webServer` (reuseExistingServer in local dev) so the browser can
 * exercise the full login + onboarding flow against real vollos-core,
 * acmd-api, and acmd-web code. Only the Google SDK and the `POST
 * /auth/google` network boundary are mocked — see src/e2e/helpers/
 * google-mock.ts.
 *
 * RSA keys are generated by scripts/generate-e2e-keys.mjs (run lazily
 * from globalSetup for the first invocation and cached on disk at
 * ./.e2e/keys.json so repeated test runs stay deterministic). The PEMs
 * are read here to pass into the vollos-core dev server and the
 * Playwright process so the test-JWT signer and the JWKS endpoint share
 * the same key pair.
 *
 * DB state is reset ONCE before the whole suite via globalSetup (see
 * src/e2e/helpers/db-reset.ts). Individual tests are responsible for
 * cleaning up any rows they create that later tests would conflict
 * with (currently only Scenario 1 → Scenario 2 share the DB; we issue
 * different emails / google_ids per scenario to avoid conflict).
 */

// Resolve RSA keys up front — playwright.config.ts runs as part of the
// initial Playwright process, so synchronous FS access is fine here. The
// helper generates + caches the pair if missing.
const keysPath = new URL('./.e2e/keys.json', `file://${CONFIG_DIR}/`).pathname;
type KeyBundle = { privateKey: string; publicKey: string };
let keys: KeyBundle;
try {
  keys = JSON.parse(readFileSync(keysPath, 'utf-8'));
} catch {
  execSync(`node "${CONFIG_DIR}/scripts/generate-e2e-keys.mjs"`, { stdio: 'inherit' });
  keys = JSON.parse(readFileSync(keysPath, 'utf-8'));
}

